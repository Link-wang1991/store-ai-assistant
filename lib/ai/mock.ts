import { ROLE_LABELS, type Role } from "../constants";

// ============================================================
// Mock 回答：无 API key 也能完整跑通流程，按结构化格式输出，
// 演示 RAG / 角色化 / 风险拦截 / 结构化（结论·原因·话术·下一步·升级·补知识库）。
// ============================================================

export type AnswerType = "knowledge" | "general" | "need_confirm" | "risk";

export function buildMockAnswer(opts: {
  role: Role;
  question: string;
  contextChunks: string[];
  answerType: AnswerType;
  roleLabel?: string;
}): string {
  const { role, question, contextChunks, answerType } = opts;
  const roleLabel = opts.roleLabel || ROLE_LABELS[role];

  if (answerType === "risk") {
    return [
      "⚠️ 这是高风险问题，我不能直接判断或给出处理结论。",
      "",
      "**结论**：不做医疗判断、不解释为「正常」「排毒」、不承诺效果。",
      "**原因**：涉及皮肤/健康异常或投诉法律风险，超出员工可处理范围。",
      "**建议话术**：「您的情况我非常重视，我马上请我们负责人来跟您对接处理。」",
      "**下一步行动**：① 安抚情绪、停止当前操作；② 立即升级；③ 如有身体不适引导就医。",
      "**是否需要升级**：是 —— 立即升级给店长 / 老板 / 专业负责人。",
      "**是否建议补充知识库**：是 —— 建议沉淀此类异常的标准处理流程。",
      "",
      "（本条已自动记录到风险记录，等待负责人跟进）",
    ].join("\n");
  }

  if (answerType === "need_confirm") {
    return [
      "**结论**：这涉及价格/活动/退款等门店政策，我不能替门店做最终决定。",
      "**原因**：政策类决定需老板/店长授权，避免私自承诺。",
      `**建议话术**（${roleLabel}视角）：「您的需求我记下了，我帮您跟店长申请一下，稍后第一时间回复您，好吗？」`,
      "**下一步行动**：把该问题提交给店长/老板确认后再回复客户。",
      "**是否需要升级**：是 —— 升级给店长/老板确认。",
      "**是否建议补充知识库**：是 —— 若属常见情况，建议补充标准口径。",
      "",
      "（本条已自动生成「待确认问题」，等待老板/店长处理）",
    ].join("\n");
  }

  if (answerType === "general" || contextChunks.length === 0) {
    return [
      "（当前知识库没有明确标准，以下是通用建议，非门店标准答案）",
      "",
      "**结论**：可以按通用思路处理，但建议尽快补充门店标准口径。",
      `**原因**：知识库暂无「${question}」的明确资料。`,
      "**建议话术**：「这个我先帮您了解清楚，给您一个最准确的答复。」",
      `**下一步行动**：${roleLabel}先了解客户真实顾虑，用门店真实案例建立信任，给出明确的下一步（预约/体验/加微信）。`,
      "**是否需要升级**：否（如涉及价格或风险则需升级）。",
      "**是否建议补充知识库**：是 —— 建议老板把该场景标准话术补进知识库。",
    ].join("\n");
  }

  // knowledge：基于检索内容
  const snippet = contextChunks
    .slice(0, 2)
    .map((c) => c.replace(/\s+/g, " ").slice(0, 140))
    .map((c, i) => `资料${i + 1}：${c}…`)
    .join("\n");

  return [
    `**结论**：可以基于门店资料回答（${roleLabel}视角）。`,
    "**原因**（引用门店知识库）：",
    snippet,
    "**建议话术**：按上面门店资料的口径表达，结合客户实际灵活调整。",
    "**下一步行动**：严格按门店资料执行，不自行加价或改活动规则。",
    "**是否需要升级**：否。",
    "**是否建议补充知识库**：否（已有明确资料）。",
  ].join("\n");
}
